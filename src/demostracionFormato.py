def main() -> None:
    uno = 1
    dos = 2
    tres = 3
    print(f"Se puede dar formato de esta forma: {uno}, {dos}, {tres}")
    tres = 4
    print(f"El ultimo digito deberia cambiar: {uno}, {dos}, {tres}")
    print(f"Tambien se puede imprimir todo junto: {uno}{dos}{tres}")
    print(f"Esto es util para RPL, se puede dar el formato exacto mas facilmente.")


main()
